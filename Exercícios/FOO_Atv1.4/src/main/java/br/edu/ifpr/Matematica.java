package br.edu.ifpr;

public class Matematica {
    double somar(double a, double b){
        return (a + b);
    }

    double subtrair(double a, double b) {
        return (a - b);
    }

    double multiplicar(double a, double b) {
        return (a * b);
    }

    double dividir(double a, double b) {
        return (a / b);
    }
}
